
def Merge(lst1, lst2):
    for letter in lst2:
        lst1.append(letter)
    return lst1 

list1 = ["1", "2", "3", "4", "5"]
list2 = ["a", "b", "c", "d", "e"]
print(Merge(list1, list2))










